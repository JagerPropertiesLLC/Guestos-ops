// app/swppp/public/[token]/page.js
//
// Public-readable SWPPP report archive. Accessed by scanning the QR code.
// Lookup is by token. Shows last 12 months of weekly reports + recent inspections.

import { getSupabaseAdmin } from '@/lib/supabaseServer';

export const dynamic = 'force-dynamic';

export default async function PublicSwpppPage({ params }) {
  const supa = getSupabaseAdmin();

  const { data: swppp } = await supa
    .from('swppp_projects')
    .select(`
      id, public_qr_active, ms4_authority, cdps_permit_number, inspection_schedule,
      project:projects!project_id(name, address)
    `)
    .eq('public_qr_token', params.token)
    .maybeSingle();

  if (!swppp || !swppp.public_qr_active) {
    return (
      <div style={{ padding: 40, fontFamily: 'system-ui', maxWidth: 600, margin: '0 auto', textAlign: 'center' }}>
        <h1>Not Available</h1>
        <p style={{ color: '#64748b' }}>This SWPPP report link is no longer active or doesn't exist.</p>
      </div>
    );
  }

  // Last 52 weekly reports + last 30 inspections
  const since = new Date(Date.now() - 365 * 86400000).toISOString().slice(0, 10);
  const [reportsRes, inspectionsRes] = await Promise.all([
    supa.from('swppp_reports')
      .select('id, period_start, period_end, inspection_count, storm_event_count')
      .eq('swppp_project_id', swppp.id)
      .gte('period_end', since)
      .order('period_end', { ascending: false }),
    supa.from('swppp_inspections')
      .select('id, inspection_date, inspection_type, inspector_name, status')
      .eq('swppp_project_id', swppp.id)
      .eq('status', 'certified')
      .gte('inspection_date', since)
      .order('inspection_date', { ascending: false })
  ]);

  const reports = reportsRes.data || [];
  const inspections = inspectionsRes.data || [];

  return (
    <div style={{ fontFamily: 'system-ui', minHeight: '100vh', background: '#f8fafc' }}>
      <header style={{ background: '#0f172a', color: '#fff', padding: '20px 24px' }}>
        <div style={{ maxWidth: 800, margin: '0 auto' }}>
          <div style={{ fontSize: 12, opacity: 0.7, letterSpacing: 0.5, textTransform: 'uppercase' }}>SWPPP Inspection Records</div>
          <h1 style={{ margin: '4px 0 6px', fontSize: 22 }}>{swppp.project?.name}</h1>
          <div style={{ fontSize: 13, opacity: 0.85 }}>{swppp.project?.address}</div>
        </div>
      </header>

      <main style={{ maxWidth: 800, margin: '0 auto', padding: '24px 20px' }}>
        <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fit, minmax(180px, 1fr))', gap: 12, marginBottom: 24 }}>
          <Stat label="MS4 Authority" value={swppp.ms4_authority || '—'} />
          <Stat label="CDPS Permit" value={swppp.cdps_permit_number || '—'} />
          <Stat label="Schedule" value={swppp.inspection_schedule === '7_day' ? 'Weekly (7-day)' : '14-day + storm'} />
        </div>

        <h2 style={{ fontSize: 13, textTransform: 'uppercase', letterSpacing: 0.6, color: '#64748b', marginTop: 28 }}>
          Weekly Reports ({reports.length})
        </h2>
        <Card>
          {reports.length === 0 && <Empty msg="No reports yet." />}
          {reports.map(r => (
            <Row key={r.id}>
              <span style={{ width: 230 }}>{r.period_start} → {r.period_end}</span>
              <span style={{ fontSize: 12, color: '#64748b' }}>
                {r.inspection_count} inspections, {r.storm_event_count} storms
              </span>
              <span style={{ flex: 1 }} />
              <a href={`/api/swppp/reports/${r.id}/pdf`} target="_blank" style={link}>View PDF →</a>
            </Row>
          ))}
        </Card>

        <h2 style={{ fontSize: 13, textTransform: 'uppercase', letterSpacing: 0.6, color: '#64748b', marginTop: 28 }}>
          Inspections (last 12 months — {inspections.length})
        </h2>
        <Card>
          {inspections.length === 0 && <Empty msg="No certified inspections in the last 12 months." />}
          {inspections.map(i => (
            <Row key={i.id}>
              <span style={{ width: 110 }}>{i.inspection_date}</span>
              <span style={{ width: 120, textTransform: 'capitalize', color: '#64748b' }}>{i.inspection_type.replace('_', ' ')}</span>
              <span style={{ flex: 1 }}>{i.inspector_name}</span>
              <a href={`/api/swppp/inspections/${i.id}/pdf`} target="_blank" style={link}>View PDF →</a>
            </Row>
          ))}
        </Card>

        <p style={{ fontSize: 11, color: '#94a3b8', textAlign: 'center', marginTop: 40 }}>
          Records maintained per Colorado CDPS COR400000 permit requirements. Generated by Duraco Properties.
        </p>
      </main>
    </div>
  );
}

function Stat({ label, value }) {
  return (
    <div style={{ background: '#fff', border: '1px solid #e2e8f0', borderRadius: 8, padding: '12px 14px' }}>
      <div style={{ fontSize: 11, color: '#64748b', textTransform: 'uppercase', letterSpacing: 0.4 }}>{label}</div>
      <div style={{ fontSize: 14, fontWeight: 500, color: '#0f172a', marginTop: 4 }}>{value}</div>
    </div>
  );
}
function Card({ children }) {
  return <div style={{ background: '#fff', border: '1px solid #e2e8f0', borderRadius: 8, marginTop: 8 }}>{children}</div>;
}
function Row({ children }) {
  return <div style={{ display: 'flex', alignItems: 'center', gap: 10, padding: '12px 16px', borderBottom: '1px solid #f1f5f9', fontSize: 14 }}>{children}</div>;
}
function Empty({ msg }) { return <div style={{ padding: 24, textAlign: 'center', color: '#64748b' }}>{msg}</div>; }
const link = { color: '#0369a1', textDecoration: 'none', fontSize: 13, fontWeight: 500 };
